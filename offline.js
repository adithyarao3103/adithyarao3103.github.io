const CACHE_NAME = 'offline-cache-v1';
const OFFLINE_URL = '/offline.html'; // path to your offline page
const OFFLINE_IMAGE = '/assets/flares.webp'; // path to your offline image

self.addEventListener('install', (event) => {
    event.waitUntil(
        caches.open(CACHE_NAME).then((cache) => {
            return cache.addAll([
                OFFLINE_URL,
                OFFLINE_IMAGE, // Add image to cache
                // Add other static assets (CSS, JS, etc.) if necessary
            ]);
        })
    );
});

self.addEventListener('fetch', (event) => {
    event.respondWith(
        caches.match(event.request).then((cachedResponse) => {
            return cachedResponse || fetch(event.request);
        }).catch(() => {
            return caches.match(OFFLINE_URL); // If all else fails, show offline page
        })
    );
});

self.addEventListener('activate', (event) => {
    const cacheWhitelist = [CACHE_NAME];
    event.waitUntil(
        caches.keys().then((cacheNames) => {
            return Promise.all(
                cacheNames.map((cacheName) => {
                    if (!cacheWhitelist.includes(cacheName)) {
                        return caches.delete(cacheName);
                    }
                })
            );
        })
    );
});
